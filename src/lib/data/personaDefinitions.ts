function w(
  p: Omit<Persona, 'steps'> & { steps?: string[] } & Partial<
      Pick<Persona, 'workplaceStats' | 'professionalGoals' | 'journeyHotspots'>
    >
): Persona {
  const steps = p.steps ?? WORK_STEPS;
  return {
    id: p.id,
    area: p.area,
    name: p.name,
    fullName: p.fullName,
    role: p.role,
    quote: p.quote,
    emoji: p.emoji,
    color: p.color,
    photo: p.photo,
    profileEyebrow: p.profileEyebrow,
    platformSegmentLabel: p.platformSegmentLabel,
    workplaceStats: p.workplaceStats,
    professionalGoals: p.professionalGoals,
    motivations: p.motivations,
    pains: p.pains,
    needs: p.needs,
    journeyMapImage: p.journeyMapImage ?? resolveJourneyMapImage(p.id, p.area),
    journeyHotspots:
      p.journeyHotspots ??
      resolveJourneyHotspots(p.id, p.area, WORK_HOTSPOTS, WORK_BLUE_COLLAR_HOTSPOTS, LEARN_HOTSPOTS, HEAL_HOTSPOTS, PLAY_HOTSPOTS),
    steps,
  };
}

